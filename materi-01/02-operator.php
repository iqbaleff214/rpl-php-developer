<?php

$a = 12;
$b = 34;
$c = true;
$d = false;
$e = [1, 2];
$f = [3, 4];

// ARITMATIKA
// + - * / % **

// ARITMATIKA PENUGASAN
// += -= *= /= %=
$a += $b; // $a = $a + $b
$b += $a; // $b = $b + $a

// PERBANDINGAN
// == === != <> !== < <= > >=
$c == $d; // false
$c !== $d; // true

// LOGIKA
// && and || or ! xor
$c and $d; // $c && $d
!$d; // true

// INCREMENT DECREMENT
$a++; // $a += $a // $a = $a + $a
++$a;
$a--;
--$a;

// ARRAY
// + == === != <> !==
$e + $f; // [1, 2, 3, 4]


